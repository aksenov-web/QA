public class For {
    public static void main(String[] args) {
        for (int i = 0; i <= 10; i++)

            if (i % 2 != 0) {
                System.out.println(i + " Odd");
            }
            else {
                System.out.println(i + " Even");

        }
        System.out.println("Test ended");


    }
}
